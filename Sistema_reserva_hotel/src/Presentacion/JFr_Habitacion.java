    private void tbl_listaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_listaMouseClicked
        // TODO add your handling code here:
        btn_guardar.setText("Editar");
        habilitar();
        btn_eliminar.setEnabled(true);
        accion = "editar";

        int fila = tbl_lista.rowAtPoint(evt.getPoint());

        txt_id_habitacion.setText(tbl_lista.getValueAt(fila, 0).toString());
        txt_num_habitacion.setText(tbl_lista.getValueAt(fila, 1).toString());
        Cmb_piso.setSelectedItem(tbl_lista.getValueAt(fila, 2).toString());
        txt_descripcion.setText(tbl_lista.getValueAt(fila, 3).toString());
        txt_caracteristicas.setText(tbl_lista.getValueAt(fila, 4).toString());
        txt_precio_diario.setText(tbl_lista.getValueAt(fila, 5).toString());
        Cmb_tipo_habitacion.setSelectedItem(tbl_lista.getValueAt(fila, 6).toString());
        Cmb_estado.setSelectedItem(tbl_lista.getValueAt(fila, 7).toString());


    }//GEN-LAST:event_tbl_listaMouseClicked

