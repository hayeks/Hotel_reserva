    private void tbl_listaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_listaMouseClicked
        // TODO add your handling code here:
        btn_guardar.setText("Editar");
        habilitar();
        btn_eliminar.setEnabled(true);
        accion = "editar";

        int fila = tbl_lista.rowAtPoint(evt.getPoint());

        txt_id_persona.setText(tbl_lista.getValueAt(fila, 0).toString());
        txt_nombre.setText(tbl_lista.getValueAt(fila, 1).toString());
        txt_apaterno.setText(tbl_lista.getValueAt(fila, 2).toString());
        txt_amaterno.setText(tbl_lista.getValueAt(fila, 3).toString());

        cmb_tipo_documento.setSelectedItem(tbl_lista.getValueAt(fila, 4).toString());
        txt_num_documento.setText(tbl_lista.getValueAt(fila, 5).toString());
        txt_direccion.setText(tbl_lista.getValueAt(fila, 6).toString());
        txt_telefono.setText(tbl_lista.getValueAt(fila, 7).toString());
        txt_email.setText(tbl_lista.getValueAt(fila, 8).toString());

        txt_sueldo.setText(tbl_lista.getValueAt(fila, 9).toString());
        cmb_acceso.setSelectedItem(tbl_lista.getValueAt(fila, 10).toString());
        txt_login.setText(tbl_lista.getValueAt(fila, 11).toString());
        txt_password.setText(tbl_lista.getValueAt(fila, 12).toString());
        cmb_estado.setSelectedItem(tbl_lista.getValueAt(fila, 13).toString());


    }//GEN-LAST:event_tbl_listaMouseClicked

