    private void btn_eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_eliminarActionPerformed
        // TODO add your handling code here:
        if (!txt_id_persona.getText().equals("")) {
            int confirmacion = JOptionPane.showConfirmDialog(rootPane, "Desea eliminar el cliente?", "Confirmacion", 2);

            if (confirmacion == 0) {
                C_Trabajador dts = new C_Trabajador();
                F_Trabajador func = new F_Trabajador();

                dts.setId_persona(Integer.parseInt(txt_id_persona.getText()));
                func.eliminar(dts);
                mostrar("");
                inhabilitar();
            }

        }
    }//GEN-LAST:event_btn_eliminarActionPerformed

