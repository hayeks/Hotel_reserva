    private void btn_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guardarActionPerformed
        // TODO add your handling code here:
        if (txt_nombre.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debe ingresar un nombre para trabajador");
            txt_nombre.requestFocus();
            return;
        }
        if (txt_apaterno.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debe ingresar el apellido paterno");
            txt_apaterno.requestFocus();
            return;
        }
        if (txt_amaterno.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debe ingresar el apellido materno");
            txt_amaterno.requestFocus();
            return;
        }
        if (txt_num_documento.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debe ingresar el número de documento");
            txt_num_documento.requestFocus();
            return;
        }
        if (txt_sueldo.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debe ingresar un sueldo para el trabajador");
            txt_sueldo.requestFocus();
            return;
        }
        if (txt_login.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debe ingresar un login para el trabajador");
            txt_login.requestFocus();
            return;
        }
        if (txt_password.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debe ingresar un password para el trabajador");
            txt_password.requestFocus();
            return;
        }

        C_Trabajador dts = new C_Trabajador();
        F_Trabajador func = new F_Trabajador();

        dts.setNombre(txt_nombre.getText());
        dts.setApe_paterno(txt_apaterno.getText());
        dts.setApe_materno(txt_amaterno.getText());

        int seleccionado = cmb_tipo_documento.getSelectedIndex();
        dts.setTipo_documento(cmb_tipo_documento.getItemAt(seleccionado));
        dts.setNum_documento(txt_num_documento.getText());
        dts.setDireccion(txt_direccion.getText());
        dts.setTelefono(txt_telefono.getText());
        dts.setEmail(txt_email.getText());

        dts.setSueldo(Double.parseDouble(txt_sueldo.getText()));
        seleccionado = cmb_acceso.getSelectedIndex();
        dts.setAcceso((String) cmb_acceso.getItemAt(seleccionado));
        dts.setLogin(txt_login.getText());
        dts.setPassword(txt_password.getText());
        seleccionado = cmb_estado.getSelectedIndex();
        dts.setEstado((String) cmb_estado.getItemAt(seleccionado));

        if (accion.equals("guardar")) {
            if (func.insertar(dts)) {
                JOptionPane.showMessageDialog(rootPane, "El trabajador fue registrada satisfactoriamente");
                mostrar("");
                inhabilitar();
            }
        } else if (accion.equals("editar")) {
            dts.setId_persona(Integer.parseInt(txt_id_persona.getText()));
            if (func.editar(dts)) {
                JOptionPane.showMessageDialog(rootPane, "El trabajador fue editada satisfactoriamente");
                mostrar("");
                inhabilitar();
            }
        }
    }//GEN-LAST:event_btn_guardarActionPerformed

